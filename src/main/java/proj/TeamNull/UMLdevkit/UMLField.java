package proj.TeamNull.UMLdevkit;

public class UMLField {
    private String name;
    private String type;

    public UMLField(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }
}
