package umleditor.sprint2.demo;

/**
 * This is only for Demoing J-Mocking
 */

public class User {

  private String id;
  private String name;

  // Constructor, getters, and setters
  public User(String id, String name) {
    this.id = id;
    this.name = name;
  }

  public String getId() {
    return id;
  }

  public String getName() {
    return name;
  }
}
